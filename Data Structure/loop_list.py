letters = ['a', 'b', 'c', 'd']

# simple Looping
for letter in letters:
    print(letter)

# looping with index and gives in tuples
for letter in enumerate(letters):
    print(letter)


# print index and contnet

for index, leter in enumerate(letters):
    print(index, leter)
    print(index)
    print(leter)
